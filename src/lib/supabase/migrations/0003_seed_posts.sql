## The kitchen: where grease hides

The kitchen accumulates grease in places you rarely notice until you're doing a proper deep clean. Here's what most people miss:

1. The range hood filter — remove it, soak in hot soapy water for 15 minutes, scrub, rinse.
2. Behind and under appliances — pull out the fridge and stove to wipe down walls, coils, and floors.
3. Cabinet doors and handles — use a degreaser, not just an all-purpose cleaner.
4. Dishwasher interior — run an empty cycle with a cup of white vinegar on the top rack.

## Bathrooms: beyond the obvious

Most people regularly clean the toilet bowl, sink, and mirror — but spring cleaning means going further. Scrub grout with a stiff brush and a paste of baking soda and water. Clean showerhead buildup by tying a bag of white vinegar around it overnight. Replace the shower liner if it has visible mold.

